function numberWithCommas(number) {
    return number.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");
}

// Function to start counting from 0 to a specified number with commas
function countTo(target, duration, counterElement) {
    const counter = document.getElementById(counterElement);
    const increment = target / duration * 15;
    let current = 0;

    const updateCounter = () => {
        current += increment;
        // Ensure the counter does not exceed the target
        if (current >= target) {
            current = target;
        }

        counter.innerText = numberWithCommas(Math.floor(current));

        if (current < target) {
            requestAnimationFrame(updateCounter);
        }
    };

    updateCounter();
}

// Using Intersection Observer to trigger counting when the section is in view
const observerOptions = { threshold: 0.5 };

const counterSection1000 = document.getElementById('counter-section-1000');
const observer1000 = new IntersectionObserver(entries => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            // Start counting from 0 to 1000 (exactly) with commas
            countTo(1000, 3000, 'counter-1000'); // 3000ms (3 seconds) duration
            observer1000.unobserve(entry.target);
        }
    });
}, observerOptions);

const counterSection12000 = document.getElementById('counter-section-12000');
